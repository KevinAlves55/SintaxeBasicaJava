package Basico;

public class IncrementoDecremento {

	public static void main(String[] args) {
		
		int a = 1;
		
		// Pré Incremento
		System.out.println(a++);
		System.out.println(a);
		
		// Incremento
		System.out.println(++a);
		System.out.println(a);
		
		// Pré Decremento
		System.out.println(a--);
		System.out.println(a);
		
		// Decremento
		System.out.println(--a);
		System.out.println(a);
		
	}

}
