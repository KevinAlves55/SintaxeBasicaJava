package Basico;

public class OperadoresDeAtribuicaoCompostos {

	public static void main(String[] args) {
		
		int numero = 10;
		
		numero += 3; // numero = numero +3
		System.out.println(numero);
		
		numero -= 2; // numero = numero -2
		System.out.println(numero);
		
		numero *= 4;
		System.out.println(numero);
		
		numero /= 2;
		System.out.println(numero);

	}

}
